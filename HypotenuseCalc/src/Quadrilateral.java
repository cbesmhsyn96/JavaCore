public class Quadrilateral {
    private int shorterLeg;
    private int longerLeg;
    private int legForSquare;
    private int anotherLeg;

    public int getShorterLeg() {
        return shorterLeg;
    }

    public void setShorterLeg(int shorterLeg) {
        this.shorterLeg = shorterLeg;
    }

    public int getLongerLeg() {
        return longerLeg;
    }

    public void setLongerLeg(int longerLeg) {
        this.longerLeg = longerLeg;
    }

    public int getLegForSquare() {
        return legForSquare;
    }

    public void setLegForSquare(int legForSquare) {
        this.legForSquare = legForSquare;
    }

    public int getAnotherLeg() {
        return anotherLeg;
    }

    public void setAnotherLeg(int anotherLeg) {
        this.anotherLeg = anotherLeg;
    }
}
